    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        double[] values = getTotalIncomeAndExpenseInTL();
        double incomeTL = values[0];
        double expenseTL = values[1];

        BarChartPanel chart = new BarChartPanel(incomeTL, expenseTL);
        chart.setBounds(0, 0, panelChartArea.getWidth(), panelChartArea.getHeight());

        panelChartArea.removeAll();
        panelChartArea.add(chart);
        panelChartArea.repaint();
    }//GEN-LAST:event_formWindowOpened
    public double[] getTotalIncomeAndExpenseInTL() {
        double incomeTL = 0;
        double expenseTL = 0;

        try {
            double rateUSD = ExchangeRateUtil.getExchangeRate("USD", "TRY");
            double rateEUR = ExchangeRateUtil.getExchangeRate("EUR", "TRY");

            Connection conn = DBConnection.getConnection();
            String sql = "SELECT type, amount, currency FROM transactions";
            PreparedStatement stmt = conn.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();

            while (rs.next()) {
                String type = rs.getString("type");
                double amount = rs.getDouble("amount");
                String currency = rs.getString("currency");

                // Ã‡eviri
                if (currency.equals("USD")) {
                    amount *= rateUSD;
                } else if (currency.equals("EUR")) {
                    amount *= rateEUR;
                }

                // Toplama
                if (type.equalsIgnoreCase("income")) {
                    incomeTL += amount;
                } else if (type.equalsIgnoreCase("expense")) {
                    expenseTL += amount;
                }
            }

            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

        return new double[]{incomeTL, expenseTL};
    }

